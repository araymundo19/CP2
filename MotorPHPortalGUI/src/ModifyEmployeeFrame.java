    private void btnRegisterEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterEmployeeActionPerformed
        
        // Load current employees from the CSVHelper
        List<String[]> employees = CSVHelper.loadEmployeeData();
        String targetId = txtEmployeeId.getText().trim();
        
        boolean updated = false;
        
        //Validation agains
        // Validate required fields
        if (txtFirstName.getText().trim().isEmpty() || txtLastName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "First and Last Name are required.");
            return;
        }
        
        // Validate birthday format
        String birthdayInput = txtBirthday.getText().trim();
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        sdf.setLenient(false);
        try {
            sdf.parse(birthdayInput);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Invalid birthday format. Use dd-MM-yyyy.");
           return;
    }
        
        // Validate salary format to double
        try {
            Double.parseDouble(txtBasicSalary.getText().trim());
            Double.parseDouble(txtRiceSubsidy.getText().trim());
            Double.parseDouble(txtPhoneAllowance.getText().trim());
            Double.parseDouble(txtClothingAllowance.getText().trim());
            Double.parseDouble(txtSemiGross.getText().trim());
            Double.parseDouble(txtHourlyRate.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers in salary/allowance fields.");
            return;
        }
        
         // Loop through employees and replace matching one
        for (int i = 0; i < employees.size(); i++) {
            if (employees.get(i)[0].equals(targetId)) {
                String[] updatedEmployee = new String[] {
                    txtEmployeeId.getText().trim(),
                    txtFirstName.getText().trim(),
                    txtLastName.getText().trim(),
                    birthdayInput,
                    txtAddress.getText().trim(),
                    txtPhone.getText().trim(),
                    txtSSS.getText().trim(),
                    txtPhilHealth.getText().trim(),
                    txtTIN.getText().trim(),
                    txtPagIbig.getText().trim(),
                    txtStatus.getText().trim(),
                    txtPosition.getText().trim(),
                    txtSupervisor.getText().trim(),
                    txtBasicSalary.getText().trim(),
                    txtRiceSubsidy.getText().trim(),
                    txtPhoneAllowance.getText().trim(),
                    txtClothingAllowance.getText().trim(),
                    txtSemiGross.getText().trim(),
                    txtHourlyRate.getText().trim()
                };

                employees.set(i, updatedEmployee);
                updated = true;
                break;
            }
        }

        if (updated) {
            CSVHelper.saveEmployeeData(employees);
            JOptionPane.showMessageDialog(this, "Employee updated successfully!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Employee not found.");
        }
    }//GEN-LAST:event_btnRegisterEmployeeActionPerformed

