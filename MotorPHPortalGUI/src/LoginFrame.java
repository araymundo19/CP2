    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String enteredId = txtEmployeeId.getText().trim();
        String enteredPass = new String(txtPassword.getPassword()).trim();

        boolean loginSuccess = false;
        
        // Login Accounts CSV Loader
        for (String[] account : CSVHelper.loadAccountData()) {
            String id = account[0].trim();      // Employee #
            String pass = account[1].trim();    // Password

            if (enteredId.equals(id) && enteredPass.equals(pass)) {
                loginSuccess = true;
                break;
            }
        }

    if (loginSuccess) {
        lblStatus.setText("Login successful.");
        // Open the MainFrame and pass the ID
        MainFrame main = new MainFrame(enteredId);
        main.setVisible(true);
        this.dispose(); // close login
    } else {
        lblStatus.setText("Invalid employee ID or password.");
    }
    }//GEN-LAST:event_btnLoginActionPerformed

