    public MainFrame(String employeeId, String role) {
    initComponents();
    setLocationRelativeTo(null);
    
    this.employeeId = employeeId;
    this.role = role;
    
    updateButtonForRole();

    // Get employee's full name and position
    String[] info = getEmployeeNameAndPosition(employeeId);
    this.fullName = info[0];
    this.position = info[1];

    // Display welcome message
    lblFirstMessage.setText("Welcome " + fullName + " - " + position + "!");
    lblRole.setText(role + " access");

    if ("User".equalsIgnoreCase(role)) {
        btnViewEmployeeList.setText("View Employee Details");
        btnViewEmployeeList.removeActionListener(btnViewEmployeeList.getActionListeners()[0]);
        btnViewEmployeeList.addActionListener(e -> {
            new EmployeeDetailsFrame(employeeId).setVisible(true);
        });
    }
}
    
private String[] getEmployeeNameAndPosition(String employeeId) {
    List<String[]> employees = CSVHelper.loadEmployeeData();
    for (String[] emp : employees) {
        if (emp[0].equals(employeeId)) {
            String fullName = emp[2] + " " + emp[1]; // First + Last
            String position = emp[11];
            return new String[]{fullName, position};
        }
    }
    return new String[]{"Employee", "Unknown"};
}

private void updateButtonForRole() {
    if ("user".equalsIgnoreCase(role)) {
        btnViewEmployeeList.setText("View Employee Details");
    }
}

public MainFrame() {
    this("Unknown", "User");
}

